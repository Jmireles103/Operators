fun main() {
    val a = 45
    val b = 15


    val ans1 = a + b
    println("$a + $b = $ans1")
    val ans2 = a - b
    println("$a - $b = $ans2")
    val ans3 = a * b
    println("$a * $b = $ans3")
    val ans4 = a / b
    println("$a / $b = $ans4")
    val ans5 = a % b
    println("$a % $b = $ans5")
}